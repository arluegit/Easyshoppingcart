        <h4 id="Lc">歐洲深山鍬形蟲</h4> 
        <img src="images/b1/Lc1.jpg" alt="Lc1" class="myimage">
        
           <p>學名:Lucanus cervus (Lc)</p>
        <p>
            敘述: 該物種是歐洲體型最大的深山鍬形蟲，身長最長可達104mm，巨大的大顎長度可達4公分。一種非常大而強大的甲蟲，一些亞種的雄性可以長到10厘米長。上面是相當空白的。狍的頭和胸是黑色的，而覆羽則是深栗褐色。雄性有細長的栗褐色上顎。在較大的個體中，它們是彎曲的、鹿形的，最長可達 3 厘米。像通常的盧卡努斯屬一樣，雄性在頭部後部兩側有高橫鰓，這些在大型個體中最為發達。
        </p>
        
