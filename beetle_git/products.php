        <h4 id="La">印尼金鍬</h4>
        <img src="images/b1/La.jpg" alt="La" class="myimage">
        
           <p>學名:Lamprima adolphinae (La)</p>
        <p>
            敘述:成蟲大顎的力量小（公母都一樣），個性也溫和。由於蟲體較小，因此食量也小，一顆果凍常常能吃個好幾天！一般來說，公蟲體型有40mm，但是特大型的公蟲則可達50mm以上。印金的頭部小，但常有明顯光澤，上方一V字型隆起十分明顯。前胸背板寬大而呈半圓形，靠近鞘翅的邊緣則帶有彎曲弧度。
        </p>

