        <h4 id="Pm">彩虹鍬形蟲</h4>
        <img src="images/b1/Pm.jpg" alt="Pm" class="myimage">
        
           <p>學名:Phalacrognathus mulleri (Pm)</p>
        <p>
            敘述:虹鍬甲屬于鞘翅目，鍬甲科，一屬一種。它們充滿金屬質感的翅鞘所蘊藏的美麗色澤是物理色，即使被製成標本也不會減退，是大自然鬼斧神工的真實寫照。是全世界最美麗的甲蟲之一。彩虹鍬甲的雄蟲體長50mm左右，雌蟲體長30mm左右。
        </p>

